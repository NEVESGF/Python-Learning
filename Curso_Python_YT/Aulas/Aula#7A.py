nome = input('Qual é o seu nome? ')
print('Prazer em te conhecer {:20}!'.format(nome))
print('Prazer em te conhecer {:>20}!'.format(nome))
print('Prazer em te conhecer {:<20}!'.format(nome))
print('Prazer em te conhecer {:^20}!'.format(nome))
print('Prazer em te conhecer {:=^20}!'.format(nome))

#Quando coloca os dois pontos entre colchetes com uma numeração, 
#é acrescentado aquela quantidade de linhas.

# Se colocar a > ele alinha a direita
# Se colocar a < ele alinha a esquerda
# Se colocar a ^ ele centraliza
# Se colocar algum digito após os dois pontos ele preenche com aquele digito
